	<div class="comentarios">
		<p> <strong>Comentário:</strong></p>

		<p> a) O protocolo de Kyoto, apesar de ser realmente uma inovação nas políticas globais do meio ambiente e
			fixar metas para a redução de gases do efeito estufa, não tem como características a criação de um
			sistema de crédito de emissão entre os países, por isso essa resposta está errada. </p>

		<p>b) Essa resposta aborda justamente o cerne do protocolo de Kyoto, que é a busca pela redução da emissão
			de gases do efeito estufa, por isso é a resposta certa. </p>

		<p> c) O protocolo não estabelece os mesmos níveis de emissão de gases, as metas de redução de gases não
			são homogêneas a todos os países, colocando níveis diferenciados de redução para os 38 países que mais
			emitem gases. O protocolo prevê ainda a diminuição da emissão de gases dos países que compõem a União
			Europeia em 8%, dos Estados Unidos em 7% e Japão em 6%. Países em franco desenvolvimento, como Brasil,
			México, Argentina, Índia e, principalmente, China, não receberam metas de redução, pelo menos
			momentaneamente. </p>

		<p>d) O protocolo de Kyoto não tem como objetivo reduzir a industrialização no mundo, e sim a emissão de
			gases. Os países emergentes não foram incluídos, em um primeiro momento, na lista dos países que devem
			diminuir a emissão de gases, apesar deste ter sido uma vontade expressa pelos países centrais. </p>

		<p>e) Países desenvolvidos não foram os “carros-chefes” nessa discussão que resultou no protocolo de Kyoto,
			pelo contrário, os Estados Unidos assinaram recentemente, mas não ratificaram o protocolo, apesar de os
			EUA
			terem feito um acordo de redução da emissão de gases recentemente com a China. </p>


