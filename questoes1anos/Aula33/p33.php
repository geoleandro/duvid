		<p class="p2">Questão 08</p>

		<p> (UFPA) Dados do Protocolo de Kyoto indicam que em 1990 países como Alemanha, Austrália, Canadá,
			Estados Unidos da América, Federação Russa, Reino Unido da Grã-Bretanha e Irlanda do Norte, França,
			Itália, Japão e Polônia eram responsáveis por cerca de 87% das emissões de CO2 na atmosfera. Em relação
			a esse Protocolo é correto afirmar:</p>

		<p class="bordaQuestoes" id="q8a"><input type="radio" required="required" name="pq8" value="a"> a)
			O Protocolo de Kyoto representa uma grande inovação nas políticas globais para o meio ambiente, pois,
			além de fixar uma meta de redução sobre os níveis de emissão de gases na atmosfera, cria um sistema de
			créditos de emissões entre países.</p>

		<p class="bordaQuestoes" id="q8b"><input type="radio" required="required" name="pq8" value="b"> b)
			O Protocolo de Kyoto determina a todos os países que, em curto prazo, estes reduzam os níveis de emissão
			de gases responsáveis pelo efeito estufa no planeta.</p>

		<p class="bordaQuestoes" id="q8c"><input type="radio" required="required" name="pq8" value="c"> c)
			O Protocolo de Kyoto estabelece os mesmo níveis de emissão de gases (CO2) conforme os padrões de
			industrialização, bem como o modelo energético adotado pelas economias nacionais.</p>

		<p class="bordaQuestoes" id="q8d"><input type="radio" required="required" name="pq8" value="d"> d)
			O Protocolo de Kyoto tem como meta reduzir a industrialização no mundo. Países como China, Brasil, Índia
			e México, que experimentam forte crescimento econômico, vivenciam sérios problemas gerados por serem
			obrigados a reduzir seu crescimento.</p>

		<p class="bordaQuestoes" id="q8e"><input type="radio" required="required" name="pq8" value="e"> e)
			O Protocolo de Kyoto resultou de negociações da Convenção sobre Mudanças Climáticas Globais, que foram
			fruto de um acordo liderado pelos Estados Unidos, tendo em oposição a União Europeia.</p>
